def _is_arabic_letter(c):
    cp = ord(c)
    return (0x621 <= cp <= 0x63A or 0x641 <= cp <= 0x64A or 0x640 == cp or
            0x66E <= cp <= 0x6D5 or 0x6EE <= cp <= 0x6FF or 0xFE70 <= cp <= 0xFEFC)


def _arabic_reshape(text):
    if not text or not isinstance(text, str):
        return text
    r = []
    for i, ch in enumerate(text):
        cp = ord(ch)
        if cp in _ARABIC_FORMS:
            prev_a = i > 0 and _is_arabic_letter(text[i-1])
            next_a = i < len(text)-1 and _is_arabic_letter(text[i+1])
            if prev_a and next_a:
                r.append(chr(_ARABIC_FORMS[cp][2]))
            elif prev_a and not next_a:
                r.append(chr(_ARABIC_FORMS[cp][3]))
            elif not prev_a and next_a:
                r.append(chr(_ARABIC_FORMS[cp][1]))
            else:
                r.append(chr(_ARABIC_FORMS[cp][0]))
        else:
            r.append(ch)
    return ''.join(r)
